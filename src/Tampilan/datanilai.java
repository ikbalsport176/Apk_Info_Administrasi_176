    protected void datatable() {
        Object[] Baris = {
            "ID Nilai",
            "NIS",
            "Nama Siswa",
            "Kelas",
            "Mata Pelajaran",
            "Kehadiran",
            "Nilai UTS",
            "Nilai UAS",
            "Nilai Tugas"};
        tabmode = new DefaultTableModel(null, Baris);
        String cariitem = txt_search.getText();

        try {
            String sql = "SELECT * FROM nilai where nis like '%" + cariitem + "%'"
                    + "or nama_siswa like '%" + cariitem + "%'"
                    + "order by id_nilai asc";
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()) {
                tabmode.addRow(new Object[]{
                    hasil.getString(1),
                    hasil.getString(2),
                    hasil.getString(3),
                    hasil.getString(4),
                    hasil.getString(5),
                    hasil.getString(6),
                    hasil.getString(7),
                    hasil.getString(8),
                    hasil.getString(9)
                });
            }
            tabel.setModel(tabmode);
        } catch (Exception e) {
        }
    }
    
