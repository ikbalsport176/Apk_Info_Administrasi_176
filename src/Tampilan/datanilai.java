    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        String sql = "insert into nilai values (?,?,?,?,?,?,?,?,?)";
        try {
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, txt_id.getText());
            stat.setString(2, txt_nis.getText());
            stat.setString(3, txt_siswa.getText());
            stat.setString(4, cb_kelas.getSelectedItem().toString());
            stat.setString(5, cb_pelajaran.getSelectedItem().toString());
            stat.setString(6, txt_khd.getText());
            stat.setString(7, txt_uts.getText());
            stat.setString(8, txt_tugas.getText());
            stat.setString(9, txt_uas.getText());
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
            kosong();
            autonumber();
            txt_nis.requestFocus();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data Gagal Disimpan " + e);
        }

        datatable();
    }//GEN-LAST:event_saveActionPerformed

