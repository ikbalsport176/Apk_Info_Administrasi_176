    public void autonumber(){
        try{
            String sql = "Select * from nilai order by id_nilai desc";
            PreparedStatement stat = conn.prepareStatement(sql);
            ResultSet rs = stat.executeQuery(sql);
            if (rs.next()){
                String nofak = rs.getString("id_nilai").substring(2);
                String AN = "" + (Integer.parseInt(nofak) + 1);
                String Nol = "";
                
                if (AN.length() == 1){
                    Nol = "000";
                }else if (AN.length() == 2){
                    Nol = "00";
                }else if (AN.length() == 3){
                    Nol = "0";
                }else if (AN.length() == 4){
                    Nol = "";
                }
                txt_id.setText("ID" + Nol + AN);
            } else {
                txt_id.setText("ID0001");
            }
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
   
