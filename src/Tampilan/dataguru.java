    private void jSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jSearchActionPerformed
        // TODO add your handling code here:
        if (txt_search.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Isikan data pencarian");
        } else {
            try {
                rs = con.querySelectAll("guru", "NIP LIKE '%" + txt_search.getText() + "%' OR nama_guru LIKE '%" + txt_search.getText() +  "%' OR alamat LIKE '%" + txt_search.getText() + "%' OR tempat_lahir LIKE '%" + txt_search.getText() + "%' OR tanggal_lahir LIKE '%" + txt_search.getText() + "%' OR jenis_kelamin LIKE '%" + txt_search.getText() + "%' OR agama LIKE '%" + txt_search.getText() + "%' OR jabatan LIKE '%" + txt_search.getText() +"%' OR pendidikan LIKE '%" + txt_search.getText() + "%' OR study LIKE '%" + txt_search.getText()+ "%'");
                tabel.setModel(new Database.ResultSetTable(rs));
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Pencarian Error");
            }
        }

    }//GEN-LAST:event_jSearchActionPerformed

