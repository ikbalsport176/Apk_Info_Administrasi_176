    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        // TODO add your handling code here:
        try {
            if (!txt_nip.getText().isEmpty() && !txt_guru.getText().isEmpty() && !txt_alamat.getText().isEmpty() && !txt_tempat.getText().isEmpty() && !txt_tgl.getText().isEmpty() && !txt_jabatan.getText().isEmpty() && !txt_pendidikan.getText().isEmpty() &&!txt_study.getText().isEmpty()) {
                String kolom[] = {"NIP", "nama_guru", "alamat", "tempat_lahir","tanggal_lahir","jenis_kelamin","agama","jabatan", "pendidikan", "study"};
                String isi[] = {txt_nip.getText(), txt_guru.getText() ,txt_alamat.getText(), txt_tempat.getText(), txt_tgl.getText(),txt_jk.getSelectedItem().toString(),txt_agama.getSelectedItem().toString(), txt_jabatan.getText(), txt_pendidikan.getText(),txt_study.getText()};

                con.queryUpdate("guru", kolom, isi, "NIP='" + String.valueOf(tabel.getValueAt(tabel.getSelectedRow(), 0)) + "'");
                JOptionPane.showMessageDialog(this, "Data Berhasil Diedit");
            } else {
                JOptionPane.showMessageDialog(this, "Data Guru ada yang kosong");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error edit data");
        }
        txt_nip.setText("");
        txt_guru.setText("");
        txt_alamat.setText("");
        txt_tempat.setText("");
        txt_tgl.setText("");
        txt_jk.setSelectedItem("");
        txt_agama.setSelectedItem("");
        txt_jabatan.getText();
        txt_pendidikan.setText("");
        txt_study.setText("");

        loadTabel();


    }//GEN-LAST:event_updateActionPerformed

    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        // TODO add your handling code here:
        try {
            String id = String.valueOf(tabel.getValueAt(tabel.getSelectedRow(), 0));
            if (JOptionPane.showConfirmDialog(this, "Yakin menghapus data", "Peringatan", JOptionPane.OK_CANCEL_OPTION) == JOptionPane.OK_OPTION) {
                con.queryDelete("guru", "NIP=" + id);
                 con.queryDelete("nilai", "id_nilai=" + id);
                  con.queryDelete("ranking", "id_nilai=" + id);

            } else {
                return;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Pilih id terlebih dahulu");
        }
        txt_nip.setText("");
        txt_guru.setText("");
        txt_alamat.setText("");
        txt_tempat.setText("");
        txt_tgl.setText("");
        txt_jk.setSelectedItem("");
        txt_agama.setSelectedItem("");
        txt_jabatan.getText();
        txt_pendidikan.setText("");
        txt_study.setText("");

        loadTabel();


    }//GEN-LAST:event_deleteActionPerformed

